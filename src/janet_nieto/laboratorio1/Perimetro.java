package janet_nieto.laboratorio1;
//jny
public class Perimetro {

	//atributo
	private  int lado1;
	private  int lado2;
	private  int lado3;
		
		
	//contructor

		public Perimetro(int plado1, int plado2, int plado3) {
			super();
			this.lado1 = plado1;
			this.lado2 = plado2;
			this.lado3 = plado3;
		}

	 //get and set
		
		public int getLado1() {
			return lado1;
		}


		public void setLado1(int lado1) {
			this.lado1 = lado1;
		}


		public int getLado2() {
			return lado2;
		}


		public void setLado2(int lado2) {
			this.lado2 = lado2;
		}


		public int getLado3() {
			return lado3;
		}


		public void setLado3(int lado3) {
			this.lado3 = lado3;
		}  



		
		
		

	
}


