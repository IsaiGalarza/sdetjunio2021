package anais_pinpingos.laboratorio2;

public class Objeto {

	private float atributo1;
	private float atributo2;
	
	public Objeto(float atributo1, float atributo2) {
		super();
		this.atributo1 = atributo1;
		this.atributo2 = atributo2;
	}

	public float getAtributo1() {
		return atributo1;
	}

	public void setAtributo1(float atributo1) {
		this.atributo1 = atributo1;
	}

	public float getAtributo2() {
		return atributo2;
	}

	public void setAtributo2(float atributo2) {
		this.atributo2 = atributo2;
	}
	
	
}
